# Визначте основну функцію для обгортання основної логіки програми.
def main():
    # Запитайте користувача ввести розмір квадратної матриці.
    n_rows = int(input("Введіть розмір квадратної матриці: "))

    # Запитайте користувача ввести символ-заповнювач.
    filler = input("\nВведіть символ-заповнювач: ")

    # Перевірте, чи введений символ-заповнювач не є одним символом.
    if len(filler) != 1:
        print("\nНе коректний символ заповнювач")
        return

    # Розрахуйте правильну кількість рядків для матриці (половина від розміру користувацького вводу).
    correct_n_rows = n_rows // 2

    # Ініціалізуйте двовимірну матрицю порожніми рядками, використовуючи спискове включення.
    arr = [['' for _ in range(n_rows)] for _ in range(correct_n_rows)]

    # Видобудьте перший символ символу-заповнювача для уніфікації.
    normalized_filler = filler[0]

    # Пройдіть по рядках матриці.
    for i in range(correct_n_rows):
        print()

        # Пройдіть по стовпцях поточного рядка.
        for j in range(len(arr[i])):
            # Заповніть кожен елемент матриці нормалізованим символом-заповнювачем.
            arr[i][j] = normalized_filler
            # Виведіть поточний елемент без нового рядка, розділяючи елементи пробілом.
            print(arr[i][j], end=" ")

        print()  # Перейдіть на новий рядок після завершення рядка.

# Перевірте, чи сценарій виконується безпосередньо (не імпортується як модуль).
if __name__ == "__main__":
    # Викличте основну функцію під час виконання сценарію.
    main()
